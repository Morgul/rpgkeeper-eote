function EditWeaponModal($scope, weapon)
{
    $scope.weapon = weapon;
    $scope.ranges = ['engaged', 'short', 'medium', 'long', 'extreme'];
} // end EditWeaponModal

// ---------------------------------------------------------------------------------------------------------------------

angular.module('eote.components').controller('EditWeaponModal', [
    '$scope',
    'weapon',
    EditWeaponModal
]);

